-- ---------------------------------------------------------------------------
-- TRIGGER: update interaction_messages.classification_status → 'classified'
-- SDS UC011 NF7: "system updates the interaction record with its type"
-- SRS US011 NF6: "system stores the classification result"
-- Fires after Python FastAPI service inserts classification result.
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION trigger_classification_update_message_status()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
    UPDATE interaction_messages
    SET    classification_status = 'classified',
           updated_at = NOW()
    WHERE  id = NEW.interaction_id;
    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_classification_update_message
    AFTER INSERT ON interaction_classifications
    FOR EACH ROW EXECUTE FUNCTION trigger_classification_update_message_status();

