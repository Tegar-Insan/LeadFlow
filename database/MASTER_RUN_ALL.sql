-- ---------------------------------------------------------------------------
-- VIEW: v_interaction_inbox
-- Created HERE (in 011) because it needs BOTH:
--   interaction_messages (created in 010)
--   interaction_classifications (created in 011)
-- Unified inbox for Marketing Staff — sorted by AI priority (UC012 NF4).
-- Priority: lead_to_sales(1) > complaint(2) > question(3) > praise(4) > spam(5)
-- ---------------------------------------------------------------------------
CREATE OR REPLACE VIEW v_interaction_inbox AS
WITH latest_classification AS (
    SELECT DISTINCT ON (interaction_id)
        interaction_id,
        sentiment_type,
        priority_level,
        confidence_score,
        suggested_reply,
        classified_at
    FROM interaction_classifications
    ORDER BY interaction_id, classified_at DESC
)
SELECT
    im.id                                               AS interaction_id,
    im.tiktok_account_id,
    im.tiktok_message_id,
    im.tiktok_user_handle,
    im.tiktok_post_id,
    im.channel_type,
    im.message_text,
    im.classification_status,
    im.response_status,
    im.response_text,
    im.amount_message_sent,
    to_wib(im.response_sent_at)                         AS responded_at_wib,
    to_wib(im.message_created_at)                       AS message_created_wib,
    to_wib(im.fetched_at)                               AS fetched_at_wib,
    lc.sentiment_type,
    lc.priority_level,
    lc.confidence_score,
    lc.suggested_reply,
    CASE lc.sentiment_type
        WHEN 'lead_to_sales' THEN 1
        WHEN 'complaint'     THEN 2
        WHEN 'question'      THEN 3
        WHEN 'praise'        THEN 4
        WHEN 'spam'          THEN 5
        ELSE 99
    END                                                 AS priority_sort_order,
    up.full_name                                        AS responded_by_name
FROM interaction_messages    im
LEFT JOIN latest_classification lc ON lc.interaction_id = im.id
LEFT JOIN user_profiles         up ON up.user_id = im.responded_by;

COMMENT ON VIEW v_interaction_inbox IS 'Marketing Staff unified inbox. Sorted by priority_sort_order ASC. WIB times.';
